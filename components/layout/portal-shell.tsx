'use client'

import { useState } from 'react'
import Link from 'next/link'
import { usePathname, useRouter } from 'next/navigation'
import { createClient } from '@/lib/supabase/client'
import { Button } from '@/components/ui/button'
import { elementId } from '@/lib/utils/element-ids'
import type { User } from '@supabase/supabase-js'

interface Profile {
  id: string
  role: 'admin' | 'physician' | 'patient' | 'staff'
  first_name: string
  last_name: string
  email: string
  avatar_url?: string
}

interface PortalShellProps {
  user: User
  profile: Profile | null
  children: React.ReactNode
}

const navigationItems = {
  patient: [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { name: 'Health Records', href: '/patient/health', icon: DocumentIcon },
    { name: 'Appointments', href: '/patient/appointments', icon: CalendarIcon },
    { name: 'Wellness', href: '/patient/wellness', icon: HeartIcon },
    { name: 'Settings', href: '/patient/settings', icon: SettingsIcon },
  ],
  physician: [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { name: 'Patients', href: '/physician/patients', icon: UsersIcon },
    { name: 'Appointments', href: '/physician/appointments', icon: CalendarIcon },
    { name: 'CME Courses', href: '/physician/cme', icon: BookIcon },
    { name: 'Protocols', href: '/physician/protocols', icon: ClipboardIcon },
    { name: 'Referrals', href: '/physician/referrals', icon: ShareIcon },
    { name: 'Settings', href: '/physician/settings', icon: SettingsIcon },
  ],
  admin: [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { name: 'Users', href: '/admin/users', icon: UsersIcon },
    { name: 'Practices', href: '/admin/practices', icon: BuildingIcon },
    { name: 'CME Management', href: '/admin/cme', icon: BookIcon },
    { name: 'Reports', href: '/admin/reports', icon: ChartIcon },
    { name: 'Settings', href: '/admin/settings', icon: SettingsIcon },
  ],
  staff: [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { name: 'Appointments', href: '/physician/appointments', icon: CalendarIcon },
    { name: 'Patients', href: '/physician/patients', icon: UsersIcon },
  ],
}

export function PortalShell({ user, profile, children }: PortalShellProps) {
  const pathname = usePathname()
  const router = useRouter()
  const [mobileMenuOpen, setMobileMenuOpen] = useState(false)

  const role = profile?.role || 'patient'
  const navItems = navigationItems[role] || navigationItems.patient
  const displayName = profile ? `${profile.first_name} ${profile.last_name}` : user.email

  const handleLogout = async () => {
    const supabase = createClient()
    await supabase.auth.signOut()
    router.push('/login')
    router.refresh()
  }

  return (
    <div id={elementId('portal', 'layout')} className="min-h-screen bg-gray-50">
      {/* Mobile menu button */}
      <div id={elementId('portal', 'mobile-header')} className="lg:hidden fixed top-0 left-0 right-0 z-50 bg-white border-b border-gray-200 px-4 py-3 flex items-center justify-between">
        <span className="text-xl font-bold text-primary-600">Foundation Health</span>
        <button
          id={elementId('portal', 'mobile-menu-btn')}
          onClick={() => setMobileMenuOpen(!mobileMenuOpen)}
          className="p-2 rounded-lg hover:bg-gray-100"
        >
          <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            {mobileMenuOpen ? (
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            ) : (
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
            )}
          </svg>
        </button>
      </div>

      {/* Mobile menu overlay */}
      {mobileMenuOpen && (
        <div
          id={elementId('portal', 'mobile-overlay')}
          className="lg:hidden fixed inset-0 z-40 bg-black/50"
          onClick={() => setMobileMenuOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        id={elementId('portal', 'sidebar')}
        className={`fixed inset-y-0 left-0 z-50 w-64 bg-white border-r border-gray-200 transform transition-transform duration-200 ease-in-out lg:translate-x-0 ${
          mobileMenuOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        <div className="flex flex-col h-full">
          {/* Logo */}
          <div id={elementId('sidebar', 'header')} className="p-6 border-b border-gray-200 hidden lg:block">
            <Link href="/dashboard">
              <span className="text-xl font-bold text-primary-600">Foundation Health</span>
            </Link>
          </div>

          {/* User info */}
          <div id={elementId('sidebar', 'user')} className="p-4 border-b border-gray-200 mt-14 lg:mt-0">
            <div className="flex items-center gap-3">
              <div className="w-10 h-10 rounded-full bg-primary-100 flex items-center justify-center text-primary-600 font-semibold">
                {profile?.first_name?.[0] || user.email?.[0]?.toUpperCase() || 'U'}
              </div>
              <div className="flex-1 min-w-0">
                <p className="text-sm font-medium text-gray-900 truncate">{displayName}</p>
                <p className="text-xs text-gray-500 capitalize">{role}</p>
              </div>
            </div>
          </div>

          {/* Navigation */}
          <nav id={elementId('sidebar', 'nav')} className="flex-1 p-4 space-y-1 overflow-y-auto">
            {navItems.map((item) => {
              const isActive = pathname === item.href || pathname.startsWith(item.href + '/')
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  id={elementId('nav', 'link', item.name.toLowerCase().replace(/\s/g, '-'))}
                  onClick={() => setMobileMenuOpen(false)}
                  className={`flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                    isActive
                      ? 'bg-primary-50 text-primary-700'
                      : 'text-gray-600 hover:bg-gray-100 hover:text-gray-900'
                  }`}
                >
                  <item.icon className={`w-5 h-5 ${isActive ? 'text-primary-600' : 'text-gray-400'}`} />
                  {item.name}
                </Link>
              )
            })}
          </nav>

          {/* Logout */}
          <div id={elementId('sidebar', 'footer')} className="p-4 border-t border-gray-200">
            <Button
              id={elementId('btn', 'logout')}
              variant="ghost"
              className="w-full justify-start text-gray-600 hover:text-gray-900"
              onClick={handleLogout}
            >
              <LogoutIcon className="w-5 h-5 mr-3 text-gray-400" />
              Sign out
            </Button>
          </div>
        </div>
      </aside>

      {/* Main content */}
      <main id={elementId('portal', 'main')} className="lg:ml-64 min-h-screen pt-14 lg:pt-0">
        <div className="p-6 lg:p-8">
          {children}
        </div>
      </main>
    </div>
  )
}

